package kr.co.abby.youtube.vo;

public class ChannelVo {

	int seq;
	String date;
	String hh;
	String datehh;
	String regdate;
	String id;
	String title;
	String description;
	String publishedAt;
	String thumbnailUrl;
	String uploads;
	String viewCount;
	String subscriberCount;
	boolean hiddenSubscriberCount;
	String videoCount;
	
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getHh() {
		return hh;
	}
	public void setHh(String hh) {
		this.hh = hh;
	}
	public String getDatehh() {
		return datehh;
	}
	public void setDatehh(String datehh) {
		this.datehh = datehh;
	}
	public String getRegdate() {
		return regdate;
	}
	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getPublishedAt() {
		return publishedAt;
	}
	public void setPublishedAt(String publishedAt) {
		this.publishedAt = publishedAt;
	}
	public String getThumbnailUrl() {
		return thumbnailUrl;
	}
	public void setThumbnailUrl(String thumbnailUrl) {
		this.thumbnailUrl = thumbnailUrl;
	}
	public String getUploads() {
		return uploads;
	}
	public void setUploads(String uploads) {
		this.uploads = uploads;
	}
	public String getViewCount() {
		return viewCount;
	}
	public void setViewCount(String viewCount) {
		this.viewCount = viewCount;
	}
	public String getSubscriberCount() {
		return subscriberCount;
	}
	public void setSubscriberCount(String subscriberCount) {
		this.subscriberCount = subscriberCount;
	}
	public boolean isHiddenSubscriberCount() {
		return hiddenSubscriberCount;
	}
	public void setHiddenSubscriberCount(boolean hiddenSubscriberCount) {
		this.hiddenSubscriberCount = hiddenSubscriberCount;
	}
	public String getVideoCount() {
		return videoCount;
	}
	public void setVideoCount(String videoCount) {
		this.videoCount = videoCount;
	}
	
	
	
	
}
